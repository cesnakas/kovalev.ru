<p>В своей автобиографии, написанной в 1938 году Георгий Матвеевич
    пишет, что занимается сбором и применением лекарственных растений с 1883 года и 55 лет жизни
    посвятил изучению методов лечения лекарственными растениями и натуральными методами лечения
    больных, имеет массу направлений профессорами и врачами к нему больных, массовые позитивные
    отзывы врачей и пациентов.</p>